void __cdecl yy_flush_buffer(yy_buffer_state *b)
{
    if (b)
    {
        b->yy_n_chars = 0;
        *b->yy_ch_buf = 0;
        b->yy_ch_buf[1] = 0;
        b->yy_buf_pos = b->yy_ch_buf;
        b->yy_at_bol = 1;
        b->yy_buffer_status = 0;
        if (b == yy_current_buffer)
            yy_load_buffer_state();
    }
}

