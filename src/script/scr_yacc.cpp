int __cdecl yyerror()
{
    if (yychar)
    {
        if (yychar != 257)
            CompileError(g_sourcePos, "bad syntax");
    }
    else
    {
        CompileError(g_sourcePos, "unexpected end of file found");
    }
    return 0;
}

